package in.thiru;

public class Calculator {
	
	public static int add(int a,int b)
	{
		return a+b;
	}
	
	public static int substaraction(int a,int b)
	{
		return a-b;
	}
	
	public static int multiplication(int a,int b)
	{
		return a*b;
	}
	
	public static int division(int a,int b)
	{
		return a/b;
	}

}
